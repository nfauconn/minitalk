#include "../../../includes/libft.h"

int	ft_isflagpf(int c)
{
	if (c == '-' || c == '0' || c == ' ' || c == '#')
		return (1);
	if (c == '+'|| c == '*' || c == '.')
		return (1);
	return (0);
}
